# ===========================================================================
# Cross-layer regression: internal create-without-id autogenerates a
# type-prefixed id at the model chokepoint; a supplied id is preserved; and
# the model no longer marks ``id`` required (so the create tool's args_schema
# derives an optional ``id``).
# ===========================================================================


def _agent_body(description: str) -> dict[str, Any]:
    """A valid Agent body WITHOUT an id, mirroring ``_agent`` above."""
    return {
        "description": description,
        "model": AgentModel(
            provider_id="anthropic-1", model_name="claude-sonnet-4-6"
        ).model_dump(mode="json"),
        "temperature": 0.0,
        "tools": [],
        "system_prompt": ["you are a test"],
    }


@pytest.mark.asyncio
async def test_create_without_id_autogenerates(system_toolset) -> None:
    result = await system_toolset.call(
        tool_name="create_agent",
        arguments={"entity": _agent_body("no id supplied")},
    )
    assert not result.is_error, result.output

    new_id = json.loads(result.output)["id"]
    assert re.fullmatch(r"agent-[0-9a-f]{12}", new_id), new_id

    # Retrievable from storage by the autogenerated id.
    result = await system_toolset.call(
        tool_name="get_agent", arguments={"id": new_id}
    )
    assert not result.is_error, result.output
    assert json.loads(result.output)["description"] == "no id supplied"


@pytest.mark.asyncio
async def test_create_with_supplied_id_is_kept(system_toolset) -> None:
    body = _agent_body("explicit id")
    body["id"] = "my-agent"
    result = await system_toolset.call(
        tool_name="create_agent", arguments={"entity": body}
    )
    assert not result.is_error, result.output
    assert json.loads(result.output)["id"] == "my-agent"

    result = await system_toolset.call(
        tool_name="get_agent", arguments={"id": "my-agent"}
    )
    assert not result.is_error, result.output
    assert json.loads(result.output)["id"] == "my-agent"


def test_create_tool_schema_marks_id_optional() -> None:
    # Robust, harness-free assertion: the create tool's wrapper args_schema
    # derives from the model schema, which no longer requires ``id``.
    schema = Agent.model_json_schema()
    assert "id" not in schema.get("required", [])
