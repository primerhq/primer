"""Update paths reuse the path id when the body omits it.

With ``Identifiable.id`` now optional + autogenerated on create, an
update whose body omits the id must NOT autogenerate a fresh random id
and then fail the body-vs-path mismatch check. It must adopt the path
id. A body that supplies a DIFFERENT explicit id still conflicts.

These cover the internal ``_system`` toolset update handler; the REST
PUT handler shares the same injection logic.
"""

